package com.ohoracs.basic.ex03;


public class Exam5 {
  public static void main(String[] args) {
   //
   System.out.println(78); //10진수표기
  //78이 정수 리터럴
   System.out.println(-78); 
   
   System.out.println(0116); //8진수
   System.out.println(-0116);

   System.out.println(0x4e); //16진수
   System.out.println(-0x4e);

   System.out.println(0b01001110); //2진수 고정소수점
   System.out.println(0.314e1); //e1은 10의 1승 .이 >로이동 
   System.out.println(31.4e-1); // .이 <로 이동 부동소수점

   // 논리 리터럴
   System.out.println("true");
   System.out.println("false");

   //한 문자 
   System.out.println('A');
   System.out.println('가');

   //문자열
   System.out.println("안녕하세요. 비트캠프");

  }

}